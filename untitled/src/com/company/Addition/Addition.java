package com.company.Addition;

public class Addition {
    public int  get_sum(int c,int d){
        return c+d;
    }
    public int  getMul(int c,int d){
        return c*d;
    }
    public float  get_Sub(int c,int d){
        return c-d;
    }
    public float  get_Div(int c,int d){
        return c/d;
    }
}
